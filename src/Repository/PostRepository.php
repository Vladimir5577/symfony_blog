    public function getPosts()
    {
        $qb = $this->createQueryBuilder('p')
            ->andwhere('p.is_active = :active')
            ->leftJoin('p.user_id', 'u')
            ->andWhere('u.is_active = :active')
            ->setParameter(':active', 1);

        return $qb->getQuery()->execute();
    }

